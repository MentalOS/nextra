    /// Author:     Daniel Saidi [daniel.saidi@gmail.com]
    /// Link:       http://danielsaidi.github.com/nextra
    /// </remarks>
    public enum ThreeState
    {
        Undetermined = -1,
        Unchecked = 0,
        Checked = 1
    }
}
